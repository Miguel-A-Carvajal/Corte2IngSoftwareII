    private void btnAddActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAddActionPerformed

        int stock= Integer.parseInt(txtStock.getText());
        Product nuevo = new Product(stock,productoGlobal.getName(), productoGlobal.getDescription(),productoGlobal.getCategories(),productoGlobal.getProduct_id(),productoGlobal.getPrice()*stock,productoGlobal.getUrlImagen());
        
        if (shoppingCartService.verificarExistencia(productoGlobal.getProduct_id())){
            shoppingCartService.eliminarProducto(productoGlobal.getProduct_id());
        }
        
        shoppingCartService.aniadirProducto(nuevo);
        this.dispose();
        
    }//GEN-LAST:event_btnAddActionPerformed

