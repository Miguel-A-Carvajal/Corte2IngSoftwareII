    private void txtStockKeyTyped(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txtStockKeyTyped
        limitarNumeros(evt);
        if(convertirAEntero(txtStock.getText()+evt.getKeyChar())>productoGlobal.getStock()){
            JOptionPane.showMessageDialog(null, "Error: Excede el numero de Stock de la tienda.", "Error", JOptionPane.ERROR_MESSAGE);
            evt.consume();
            txtStock.setText(String.valueOf(productoGlobal.getStock()));
        }
    }//GEN-LAST:event_txtStockKeyTyped

