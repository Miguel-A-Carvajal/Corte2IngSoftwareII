    public void limitarNumeros(KeyEvent e) {
        char c = e.getKeyChar();
        if (!Character.isDigit(c)) {
            e.consume(); // Ignorar el car치cter no v치lido
        }
    }
    
    public int convertirAEntero(String cadena) {
        try {
            return Integer.parseInt(cadena);
        } catch (NumberFormatException e) {
            // Si la conversi칩n falla, retorna 0
            return 0;
        }
    }
