    public void limitarNumeros(KeyEvent e) {
        char c = e.getKeyChar();
        if (!Character.isDigit(c)) {
            e.consume(); // Ignorar el carÃ¡cter no vÃ¡lido
        }
    }
    private Product obtenerProductoFormulario(){
        Long Id = Long.valueOf(txtId.getText());
        String nombre = txtNombre.getText();
        int Stock = Integer.parseInt(txtStock.getText());
        String descripcion = txtDescripcion.getText();
        String UrlImange = txtUrlImagen.getText();
        ArrayList<Category> aux = obtenerCategoriasProducto();
        Double Precio = Double.valueOf(txtPrecio.getText());
        String keyWords = txtKeyword.getText();
        String characteristics = txtCaracteristicas.getText();
        
        return new Product(Id, nombre, descripcion, Precio, Stock, keyWords, characteristics, UrlImange, aux);
    }
    private boolean verificarCamposVacios(){
        if(txtNombre.getText().isBlank() || txtDescripcion.getText().isBlank() || txtUrlImagen.getText().isBlank()|| lstCategoriasSeleccionada.getModel().getSize() == 0 || txtPrecio.getText().isBlank()
                || txtId.getText().isBlank() || txtCaracteristicas.getText().isBlank() || txtKeyword.getText().isBlank()){
            return true;
        }
        return false;
    }
    private ArrayList<Category> obtenerCategoriasProducto() {
        DefaultListModel model = (DefaultListModel) lstCategoriasSeleccionada.getModel();
        ArrayList<Category> elementos = new ArrayList<Category>();
        for (int i = 0; i < model.size(); i++) {
            String[] partes = ((String)model.getElementAt(i)).split("-");
            Category aux = new Category(Long.parseLong(partes[0]),partes[1]);
            elementos.add(aux);
        }
        return elementos;
    }


