    private void lstSeleccionarCategoriasMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_lstSeleccionarCategoriasMouseClicked

        DefaultListModel model = (DefaultListModel) lstCategoriasSeleccionada.getModel();
        if(!verificarCategoriaDuplicada(lstSeleccionarCategorias.getSelectedValue(),model)){
            model.addElement(lstSeleccionarCategorias.getSelectedValue());
        }
        
    }//GEN-LAST:event_lstSeleccionarCategoriasMouseClicked

    public boolean verificarCategoriaDuplicada(String categoriaAgregar,DefaultListModel model){
        for (int i = 0; i < model.size(); i++) {
            if(((String)model.getElementAt(i)).equals(categoriaAgregar)){
                return true;
            }
        }
        return false;
    }
    
    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        if(verificarCamposVacios()){
            JOptionPane.showMessageDialog(null, "Hay campos vacios", "Mensaje", JOptionPane.ERROR_MESSAGE);
            return;
        }
        Product aux = obtenerProductoFormulario();
        if(Pservice.saveProduct(aux)){
            JOptionPane.showMessageDialog(null, "Registro exitoso :3", "Mensaje", JOptionPane.INFORMATION_MESSAGE);
            this.dispose();
        }else{
            JOptionPane.showMessageDialog(null, "Error inesperado:(", "Mensaje", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_jButton1ActionPerformed

